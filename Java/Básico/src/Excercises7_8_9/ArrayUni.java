package Excercises7_8_9;

public class ArrayUni {
    public static void main(String[] args) {

        String letters[] = {"a", "b", "c", "d", "e", "f", "g", "h", "i", "j", "k"};

        for (int i = 0; i < letters.length; i++) {
            System.out.println( "letter : " + letters[i] + " " + "posicion : " + i);
        }
    }
}
