package Ejercicios;

public class DataType {

    public static void main(String[] args) {

        // 1. numbers


        // 1.1 integer
        byte variable1 = 7;
        System.out.println(variable1);
        short variable2 = 25;
        System.out.println(variable2);
        int variable3 = 500;
        System.out.println(variable3);
        long variable4 = 101;
        System.out.println(variable4);
        long numberId = 333221;
        // long numberId = null;

        // 1.2 float
        float variable5 = 7.66f;
        System.out.println(variable5);
        double variable6;
        variable6 = 67.59d;
        System.out.println(variable6);
        double price = 69.69;
        System.out.println(price);

        // 2. boolean
        boolean variable7 = false;
        System.out.println(variable7);
        boolean variable8 = true;
        System.out.println(variable8);

        // 3. text
        char variable9 = 'c';
        System.out.println(variable9);
        String variable10 = "Lorem ipsum dolor";
        System.out.println(variable10);
    }
}